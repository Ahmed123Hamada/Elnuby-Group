// Helper function to get translated content based on language
export const getTranslatedContent = (content, language = 'ar') => {
  if (typeof content === 'object' && content.ar && content.en) {
    return content[language];
  }
  return content;
};

// Helper function to get translated array content
export const getTranslatedArray = (array, language = 'ar') => {
  if (Array.isArray(array) && array.length > 0 && typeof array[0] === 'object' && array[0].ar) {
    return array.map(item => item[language]);
  }
  return array;
};